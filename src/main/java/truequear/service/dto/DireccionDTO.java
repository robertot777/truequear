package truequear.service.dto;

import javax.validation.constraints.*;
import java.io.Serializable;
import java.util.Objects;

/**
 * A DTO for the Direccion entity.
 */
public class DireccionDTO implements Serializable {

    private Long id;

    @NotNull
    private String direccion;

    @NotNull
    private String numero;

    private String adicional;

    private String codigoPostal;

    private Long comunaId;

    private Long tipoId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getAdicional() {
        return adicional;
    }

    public void setAdicional(String adicional) {
        this.adicional = adicional;
    }

    public String getCodigoPostal() {
        return codigoPostal;
    }

    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }

    public Long getComunaId() {
        return comunaId;
    }

    public void setComunaId(Long comunaId) {
        this.comunaId = comunaId;
    }

    public Long getTipoId() {
        return tipoId;
    }

    public void setTipoId(Long userId) {
        this.tipoId = userId;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        DireccionDTO direccionDTO = (DireccionDTO) o;
        if (direccionDTO.getId() == null || getId() == null) {
            return false;
        }
        return Objects.equals(getId(), direccionDTO.getId());
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(getId());
    }

    @Override
    public String toString() {
        return "DireccionDTO{" +
            "id=" + getId() +
            ", direccion='" + getDireccion() + "'" +
            ", numero='" + getNumero() + "'" +
            ", adicional='" + getAdicional() + "'" +
            ", codigoPostal='" + getCodigoPostal() + "'" +
            ", comuna=" + getComunaId() +
            ", tipo=" + getTipoId() +
            "}";
    }
}
